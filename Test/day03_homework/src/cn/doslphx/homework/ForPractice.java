package cn.doslphx.homework;

public class ForPractice {
    public static void main(String[] args) {
        //输入1~5和5~1
        for(int i = 1;i<=5;i++){
            System.out.println(i);
        }
        System.out.println("****************");
        for (int j =5;j>=1;j--){
            System.out.println(j);
        }
    }
}
